def generate_sql():
    """生成专业目录 SQL 文件"""
    
    # 统计专业数量
    total_majors = sum(len(majors) for majors in MAJORS_DATA.values())
    
    sql_lines = [
        "-- ============================================",
        "-- 全国普通高等学校本科专业目录",
        "-- 数据来源：教育部《普通高等学校本科专业目录（2024年版）》",
        "-- 包含14个学科门类，{}个专业".format(total_majors),
        "-- 生成时间：2025年",
        "-- ============================================",
        "",
        "truncate table sys_major_catalog;",
        "",
        "-- 使用子查询获取学科门类对应的 dict_item_id",
        "-- 注意：需要先执行 data_core.sql 插入字典数据",
        "",
    ]
    
    # 按学科门类顺序生成 SQL
    all_values = []
    
    for category_code in sorted(MAJORS_DATA.keys()):
        majors = MAJORS_DATA[category_code]
        category_name = CATEGORY_NAMES.get(category_code, '未知')
        
        sql_lines.append("-- {}（{}）- {}个专业".format(category_code, category_name, len(majors)))
        
        for major_code, major_name in majors:
            # 使用子查询获取 category_dict_item_id
            values = "('{}', '{}', (SELECT id FROM sys_dict_item WHERE dict_type = 'major_category' AND dict_value = '{}'))".format(
                major_code,
                major_name.replace("'", "''"),  # 转义单引号
                category_code
            )
            all_values.append(values)
    
    # 合并所有值，每行 3 个（避免行太长）
    sql_lines.append("")
    sql_lines.append("insert into sys_major_catalog (major_code, major_name, category_dict_item_id) values")
    sql_lines.append("")
    
    for i in range(0, len(all_values), 3):
        batch = all_values[i:i+3]
        if i + 3 >= len(all_values):
            sql_lines.append(','.join(batch) + ';')
