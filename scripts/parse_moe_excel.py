def get_city_code(province, city):
    """获取城市代码"""
    if city in CITY_CODES:
        return CITY_CODES[city]
    # 如果找不到，使用省会代码
    if province in PROVINCE_CODES:
        return PROVINCE_CODES[province][:4] + '00'
    return PROVINCE_CODES.get(province, '000000')[:4] + '00'

def parse_excel():
    """解析Excel文件"""
    df = pd.read_excel('/Users/huxiaodong/PycharmProjects/haidong-tuanwei/src/main/resources/W020250729615142156867.xls')
    
    schools = []
    current_province = None
    
    for idx, row in df.iterrows():
        # 检查是否是省份标题行
        col0 = str(row.iloc[0]) if pd.notna(row.iloc[0]) else ''
        
        # 匹配省份标题，如 "北京市（92所）"
        prov_match = re.match(r'(.+?)（(\d+)所）', col0)
        if prov_match:
            current_province = prov_match.group(1)
            continue
        
        # 跳过标题行和空行
        if col0 in ['序号', '附件1：', '全国普通高等学校名单\n（截至2025年6月20日）', 'nan', '']:
            continue
        
        # 尝试解析学校数据行
        try:
            if pd.notna(row.iloc[1]) and pd.notna(row.iloc[2]):
                school_name = str(row.iloc[1]).strip()
                school_code = str(row.iloc[2]).strip()
                
                # 跳过标题行
                if school_name == '学校名称' or school_code == '学校标识码':
                    continue
                
                # 获取主管部门和所在地
                dept = str(row.iloc[3]).strip() if pd.notna(row.iloc[3]) else current_province
                city = str(row.iloc[4]).strip() if pd.notna(row.iloc[4]) else dept
                school_type = str(row.iloc[5]).strip() if pd.notna(row.iloc[5]) else '本科'
                remark = str(row.iloc[6]).strip() if pd.notna(row.iloc[6]) and str(row.iloc[6]) != 'nan' else ''
                
                # 确定省份代码
                prov_code = PROVINCE_CODES.get(current_province, '000000')
                city_code = get_city_code(current_province, city)
                
                # 确定类别
                if school_name in DOUBLE_FIRST_CLASS:
                    category = '双一流建设'
                elif school_type == '专科':
                    category = '专科'
                else:
                    category = '本科'
                
                schools.append({
                    'code': school_code,
                    'name': school_name,
                    'type': school_type,
                    'province_code': prov_code,
                    'city_code': city_code,
                    'category': category,
                    'province': current_province
                })
        except Exception as e:
            print(f"解析第{idx}行出错: {e}")
            continue
    
    return schools

def generate_sql(schools):
    """生成SQL文件"""
    # 按省份分组
    by_province = {}
    for school in schools:
        prov = school['province']
        if prov not in by_province:
            by_province[prov] = []
        by_province[prov].append(school)
    
    sql_lines = [
        "-- ============================================",
        "-- 全国高校数据 - 普通高等学校",
        "-- 数据来源：教育部全国普通高等学校名单（截至2025年6月20日）",
        "-- 学校数量：{}所".format(len(schools)),
        "-- 生成时间：2025年",
        "-- ============================================",
        "",
        "truncate table sys_school;",
        "",
        "insert into sys_school (school_code, school_name, school_type, province_code, city_code, category, create_by, update_by, deleted) values",
        ""
    ]
    
    all_values = []
    
    for prov_name in sorted(by_province.keys(), key=lambda x: PROVINCE_CODES.get(x, '999999')):
        schools_in_prov = by_province[prov_name]
        
        sql_lines.append("-- {}（{}所）".format(prov_name, len(schools_in_prov)))
        
        for school in schools_in_prov:
            values = "('{}', '{}', '{}', '{}', '{}', '{}', 1, 1, 0)".format(
                school['code'],
                school['name'],
                school['type'],
                school['province_code'],
                school['city_code'],
                school['category']
            )
            all_values.append(values)
    
    # 合并所有值，每行5个（避免行太长）
    for i in range(0, len(all_values), 5):
        batch = all_values[i:i+5]
        if i + 5 >= len(all_values):
            sql_lines.append(','.join(batch) + ';')
        else:
            sql_lines.append(','.join(batch) + ',')
    
    return '\n'.join(sql_lines)

