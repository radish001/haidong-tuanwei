# 提取县级行政区（代码第5-6位不为00）
counties_by_province = {}
for code, name in regions:
    province_code = code[:2]
    prefecture_code = code[:4]

    # 跳过省级（后4位为0000）和地级（后2位为00）
    if code[2:] == '0000':  # 省级
        continue
    if code[4:] == '00':  # 地级
        continue

    # 跳过重定向条目如"市辖区"
    if name in ['市辖区', '县', '省直辖县级行政区划', '自治区直辖县级行政区划']:
        continue

    # 跳过特殊标记的条目（带*号的虚构区域）
    if '*' in name:
        continue

    # 确定parent_id（地级行政区的id）
    parent_id = prefecture_ids.get(prefecture_code)
    if not parent_id:
        # 对于省直辖县级，parent_id为省级id
        if prefecture_code in ['4190', '4290', '4690', '6590']:
            parent_id = province_codes.get(province_code)
        else:
            print(f"Warning: 未找到 {code} {name} 的parent_id (prefecture: {prefecture_code})")
            continue

    if province_code not in counties_by_province:
        counties_by_province[province_code] = []

    counties_by_province[province_code].append({
        'code': code,
        'name': name,
        'parent_id': parent_id,
        'prefecture_code': prefecture_code,
    })

